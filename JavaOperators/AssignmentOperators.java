public class AssignmentOperators {

    public int assignment(int x, int y) {
        return x = y;
    }

    public int addAndAssignment(int x, int y) {
        return x += y;
    }

    public int subtractAndAssignment(int x, int y) {
        return x -= y;
    }

    public int multiplyAndAssignment(int x, int y) {
        return x *= y;
    }

    public int divideAndAssignment(int x, int y) {
        return x /= y;
    }

    public int modulusAndAssignment(int x, int y) {
        return x %= y;
    }

    public int leftShiftAndAssignment(int x, int y) {
        return x <<= y;
    }

    public int rightShiftAndAssignment(int x, int y) {
        return x >>= y;
    }

    public int bitwiseAndAssignment(int x, int y) {
        return x &= y;
    }

    public int bitwiseExclusiveOrAssignment(int x, int y) {
        return x ^= y;
    }

    public int bitwiseInclusiveOrAssignment(int x, int y) {
        return x |= y;
    }
}
