public class ComparisonOperators {

    public boolean equalTo(int x, int y) {
        return x == y;
    }

    public boolean notEqualTo(int x, int y) {
        return x != y;
    }

    public boolean greaterThan(int x, int y) {
        return x > y;
    }

    public boolean lessThan(int x, int y) {
        return x < y;
    }

    public boolean greaterThanOrEqualTo(int x, int y) {
        return x >= y;
    }

    public boolean lessThanOrEqualTo(int x, int y) {
        return x <= y;
    }
}
